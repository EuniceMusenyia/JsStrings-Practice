// Finding the length of a string
const one = "I love rain";
console.log(one.length);

let sentence = "JS love JS";
console.log(sentence.length);


// replace
console.log(sentence.replace("JS loves JS","I enjoy JS"));

// Finding the index
console.log(sentence.indexOf("JS"));
console.log(sentence);

// Finding the last index
console.log(sentence.lastIndexOf("JS"));

// Start with /ends with
console.log(sentence.startsWith("JS"));
console.log(sentence.endsWith("J"));

// Upper case/ lower case
console.log(sentence.toUpperCase());
console.log(sentence.toLowerCase());

// string includes
console.log(sentence.includes("a"));
console.log(sentence.includes("e"));

// string repeat
console.log(sentence.repeat(3));

// charAt->returns the character at a specified index in a string or prints the number given in a string
console.log(sentence.charAt("5"));

// substring
console.log(sentence.substring(4));

